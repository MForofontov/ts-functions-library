/**
 * Calculates the decimal logarithm (base 10) of a number.
 * 
 * @param n - The number to calculate the logarithm of.
 * @returns The base 10 logarithm of the number.
 */
export function calculatLogBase10(n: number): number {
    return Math.log10(n);
}

