/**
 * Gets an array of the days of the week.
 *
 * @returns An array of strings representing the days of the week.
 */
export function getDaysOfWeek(): string[] {
  return [
    'Sunday',
    'Monday',
    'Tuesday',
    'Wednesday',
    'Thursday',
    'Friday',
    'Saturday',
  ];
}

